// expresión (?) 'Es true' (:) 'Es false'

let edad = 16;

// El operador ternario se usa para hacer una evaluación rápida tipo if - else
// Sintaxis: condición ? valor_si_verdadero : valor_si_falso

let acceso = edad > 17 ? 'Si puede ingresar' : 'No puede ingresar';// edad > 17 la edad es mayor que 17?
// (?) si la condición es verdadera, se ejecuta el primer valor puede ingresar
// ():)si la condición es falsa, se ejecuta el segundo valor no puede ingresar

console.log(acceso); 
